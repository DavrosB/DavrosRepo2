package GarageEx;

public class Vehicle {
	
	String name;
	String reg;
	int wheels;
	String fuel;
	
	
	public Vehicle(String name, String reg, int wheels, String fuel) {
		super();
		this.name = name;
		this.reg = reg;
		this.wheels = wheels;
		this.fuel = fuel;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getReg() {
		return reg;
	}


	public void setReg(String reg) {
		this.reg = reg;
	}


	public int getWheels() {
		return wheels;
	}


	public void setWheels(int wheels) {
		this.wheels = wheels;
	}


	public String getFuel() {
		return fuel;
	}


	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
	
	
	
	
	
	


	

		
	
	
	
	
	
}
